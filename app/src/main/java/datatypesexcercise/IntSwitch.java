package datatypesexcercise;

public class IntSwitch {

    public void SwitchInts(){
        int a = 10;
        int b = 20;

        System.out.print("a = " + a);
        System.out.print(" ");
        System.out.println("b = " + b);

        a = b;
        b -= 10;

        System.out.print("a = " + a);
        System.out.print(" ");
        System.out.println("b = " + b);

    }
}
